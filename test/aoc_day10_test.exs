defmodule AoCDay10Test do
  use ExUnit.Case

  test "part1" do
    assert AoC.Day10.part1("inputs/day10.sample") == 13140
    assert AoC.Day10.part1("inputs/day10") == 15120
  end

  test "part2" do
    assert AoC.Day10.part2("inputs/day10.sample") == [
             '##..##..##..##..##..##..##..##..##..##..',
             '###...###...###...###...###...###...###.',
             '####....####....####....####....####....',
             '#####.....#####.....#####.....#####.....',
             '######......######......######......####',
             '#######.......#######.......#######.....'
           ]

    assert AoC.Day10.part2("inputs/day10") == [
             '###..#..#.###....##.###..###..#.....##..',
             '#..#.#.#..#..#....#.#..#.#..#.#....#..#.',
             '#..#.##...#..#....#.###..#..#.#....#..#.',
             '###..#.#..###.....#.#..#.###..#....####.',
             '#.#..#.#..#....#..#.#..#.#....#....#..#.',
             '#..#.#..#.#.....##..###..#....####.#..#.'
           ]
  end
end
